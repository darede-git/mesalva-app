# frozen_string_literal: true

ActiveAdmin.register NodeModule do
  actions :index, :show, :update, :edit, :new, :create
  permit_params :name, :active, :description, :suggested_to, :relevancy,
                :position, :instructor_id, :image
  menu label: 'NodeModule', parent: 'Conte√∫do'

  index do
    selectable_column
    id_column
    column :name
    column :active
    column :slug
    column :created_at
    column :relevancy
    column :position
    column :instructor_id
    actions
  end

  filter :id
  filter :name
  filter :slug
  filter :created_at

  form do |f|
    f.inputs do
      f.input :name
      f.input :active
      f.input :description
      f.input :suggested_to
      f.input :relevancy
      f.input :position
      f.input :instructor_id

      f.input :image if f.object.new_record?
    end
    f.actions
  end
end
