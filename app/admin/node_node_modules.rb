# frozen_string_literal: true

ActiveAdmin.register NodeNodeModule do
  actions :index, :show, :update, :edit, :new, :create
  permit_params :position, :node_id, :node_module_id
  menu label: 'Node - NodeModule', parent: 'Conte√∫do'

  index do
    selectable_column
    id_column
    column :node_id
    column :node_module_id
    column :created_at
    column :position
    actions
  end

  filter :node_id
  filter :node_module_id
  filter :slug
  filter :created_at
  filter :ancestry

  form do |f|
    f.inputs do
      if f.object.new_record?
        f.input :node_id
        f.input :node_module_id
      end
      f.input :position
    end
    f.actions
  end
end
